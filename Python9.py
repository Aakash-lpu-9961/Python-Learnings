# Example of Python Dictionary data type program
dict={'name':'john','code':'2568','department':'sales'}
print("Values of keys in dict:\n",dict.keys())
print("Values of dict: \n",dict.values())
print("Value inside the dictionary is: \n",dict)
print("Value of first element of dict is: \n",dict['name'])
print("Value of first element of dict is: \n",dict['code'])
print("Value of first element of dict is: \n",dict['department'])